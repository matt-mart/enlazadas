void PILA_Crear (
  PILA* _pila
){

  //----------------------------------------------------------//

  if ( _pila->raiz != nullptr )  /// _pila->largo != 0
    return PILA_Borrar( _pila );

  //----------------------------------------------------------//

  _pila->raiz  = nullptr;
  _pila->largo = 0;

  //----------------------------------------------------------//

}

