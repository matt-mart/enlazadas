void PILA_Borrar (
  PILA* _pila
){

  //----------------------------------------------------------//

  PILA::NODO* ptr_nodo = _pila->raiz;

  //----------------------------------------------------------//

  while ( ptr_nodo != nullptr ){

    PILA::NODO* prox = ptr_nodo->prox;

    delete ptr_nodo;

    ptr_nodo = prox;

  }

  //----------------------------------------------------------//

  _pila->raiz  = nullptr;
  _pila->largo = 0;

  //----------------------------------------------------------//

}

